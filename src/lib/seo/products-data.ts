export const getAllProducts = (): Array<{ niche: Niche; product: ProductSubcategory }> => {
  const products: Array<{ niche: Niche; product: ProductSubcategory }> = [];

  for (const niche of NICHES) {
    const nicheProducts = NICHE_PRODUCTS[niche] || [];
    for (const product of nicheProducts) {
      products.push({ niche, product });
    }
  }

  return products;
};

export const getProductsByNiche = (niche: Niche): ProductSubcategory[] => {
  return NICHE_PRODUCTS[niche] || [];
};

export const getProductBySlug = (niche: Niche, slug: string): ProductSubcategory | undefined => {
  const products = NICHE_PRODUCTS[niche] || [];
  return products.find((p) => p.slug === slug);
};

export const getAllProductPaths = (): Array<{ niche: string; product: string }> => {
  const paths: Array<{ niche: string; product: string }> = [];

  for (const niche of NICHES) {
    const nicheProducts = NICHE_PRODUCTS[niche] || [];
    for (const product of nicheProducts) {
      paths.push({ niche, product: product.slug });
    }
  }

  return paths;
};
